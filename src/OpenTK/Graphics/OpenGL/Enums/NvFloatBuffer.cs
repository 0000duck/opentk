using System;

namespace OpenTK.Graphics.OpenGL
{
    /// <summary>
    /// Not used directly.
    /// </summary>
    public enum NvFloatBuffer
    {
        /// <summary>
        /// Original was GL_FLOAT_R_NV = 0x8880
        /// </summary>
        FloatRNv = 0x8880,

        /// <summary>
        /// Original was GL_FLOAT_RG_NV = 0x8881
        /// </summary>
        FloatRgNv = 0x8881,

        /// <summary>
        /// Original was GL_FLOAT_RGB_NV = 0x8882
        /// </summary>
        FloatRgbNv = 0x8882,

        /// <summary>
        /// Original was GL_FLOAT_RGBA_NV = 0x8883
        /// </summary>
        FloatRgbaNv = 0x8883,

        /// <summary>
        /// Original was GL_FLOAT_R16_NV = 0x8884
        /// </summary>
        FloatR16Nv = 0x8884,

        /// <summary>
        /// Original was GL_FLOAT_R32_NV = 0x8885
        /// </summary>
        FloatR32Nv = 0x8885,

        /// <summary>
        /// Original was GL_FLOAT_RG16_NV = 0x8886
        /// </summary>
        FloatRg16Nv = 0x8886,

        /// <summary>
        /// Original was GL_FLOAT_RG32_NV = 0x8887
        /// </summary>
        FloatRg32Nv = 0x8887,

        /// <summary>
        /// Original was GL_FLOAT_RGB16_NV = 0x8888
        /// </summary>
        FloatRgb16Nv = 0x8888,

        /// <summary>
        /// Original was GL_FLOAT_RGB32_NV = 0x8889
        /// </summary>
        FloatRgb32Nv = 0x8889,

        /// <summary>
        /// Original was GL_FLOAT_RGBA16_NV = 0x888A
        /// </summary>
        FloatRgba16Nv = 0x888a,

        /// <summary>
        /// Original was GL_FLOAT_RGBA32_NV = 0x888B
        /// </summary>
        FloatRgba32Nv = 0x888b,

        /// <summary>
        /// Original was GL_TEXTURE_FLOAT_COMPONENTS_NV = 0x888C
        /// </summary>
        TextureFloatComponentsNv = 0x888c,

        /// <summary>
        /// Original was GL_FLOAT_CLEAR_COLOR_VALUE_NV = 0x888D
        /// </summary>
        FloatClearColorValueNv = 0x888d,

        /// <summary>
        /// Original was GL_FLOAT_RGBA_MODE_NV = 0x888E
        /// </summary>
        FloatRgbaModeNv = 0x888e
    }
}
